/*Demonstrate the Operator precedence. 
1.  10 + 20 * 30
2.  100 / 10 * 100
3.  5 * 4 / 4 % 3
4.  100 + 200 / 10 - 3 * 10
*/

public class operatorr{
    public static void main(String[] args){
        System.out.println(10+20*30);
        System.out.println(100/10*100);
        System.out.println(5*4/4%3);
        System.out.println(100+200/10-3*10);
    }
}